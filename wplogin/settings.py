def get_secret_key(filename):
    if os.path.exists(filename) == False:
        with open(filename, 'w') as file:
            file.write(get_random_secret_key())
        
    with open(filename, 'r') as file:
        secret_keys = file.readlines()
        return secret_keys[0]


